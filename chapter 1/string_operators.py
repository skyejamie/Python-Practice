# string operators
a = "144 / 12 = "
b = 12
print(a + str(b)) # the "str()" in the print function converts the "b" variable into a string in order to link the two values together. (should print as "144 / 12 = 12" in the terminal.)

# another example
g = "b"
h = 4
print(g + str(h)) # should print as "b4"